# Data Types - Unit 2b
# Below are examples of the different data types.

# Python is a non-strict language - you don't define the specific data type of a variable.

integer = 6 # A whole number - 6, 1, 39, -2
floatingPoint = 4.53 # Number with a decimal point - 23.21, 1.0, 0.2, -0.1
character = 'b' # Single ASCII character - A, l, !, @
string = "Hello World!" # Zero or more characters - "Thank you", "Hello World", "This is a string"
boolean = False # True or False
